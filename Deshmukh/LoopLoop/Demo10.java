package LoopLoop;

public class Demo10 {

    public static void main(String[] args) {
        // sum of number from 1 to100
        int sum = 0;
        for (int i =1; i<=100; i++){
            //sum +=i ;
            sum =sum + i;
            System.out.println(" Sum of numbers from 1to 100"+ sum);
        }
    }
}


